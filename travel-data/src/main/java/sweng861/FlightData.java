package sweng861;

public class FlightData {
  public String origin_location;
  public String destination_location;
  public String departure_date;
  public String return_date;
  public Integer number_of_adults;

  public FlightData() {}

  public String getOriginLocation() { return origin_location; }
  public String getDestinationLocation() { return destination_location; }
  public String getDepartureDate() { return departure_date; }
  public String getReturnDate() { return return_date; }
  public Integer getNumberOfAdults() { return number_of_adults; }
}
;
